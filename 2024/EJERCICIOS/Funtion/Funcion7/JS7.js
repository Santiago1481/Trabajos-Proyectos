function acuadrado(plado){
    let lado = plado;
    let rescuadrado = lado * lado;
    return rescuadrado;
}
function arectangulo(pbase,paltura){
    let altura = paltura;
    let base = pbase;
    let restrectangulo = base * altura;
    return restrectangulo;
}
function atriangulo(pbase1,paltura1){
    let base1 = pbase1;
    let altura1 = paltura1;
    let resttriangulo = (base1 * altura1)/2;
    return resttriangulo;
}